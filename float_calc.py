""" Methods for float num"""


def calc(num_one: float, num_two: float, operator):
    """ Operators for float number"""
    if operator == '+':
        return num_one + num_two
    if operator == '-':
        return num_one - num_two
    if operator == '*':
        return num_one * num_two
    if operator == '/':
        return num_one / num_two
